/* ── Sidebar drawer (mobile) ────────────────────────────────── */

function toggleSidebar() {
  const sidebar = document.querySelector(".sidebar");
  const overlay = document.getElementById("sidebar-overlay");
  const isOpen  = sidebar.classList.toggle("open");
  overlay.classList.toggle("open", isOpen);
}

function closeSidebar() {
  document.querySelector(".sidebar").classList.remove("open");
  document.getElementById("sidebar-overlay").classList.remove("open");
}

window.addEventListener("popstate", e => {
  const page = e.state?.page || window.location.pathname.replace(/^\//, "") || "invoices";
  showPage(page, false);
});

/* ── Modal helpers ──────────────────────────────────────────── */

function openModal(id) {
  document.getElementById(id)?.classList.add("open");
}

function closeModal(id) {
  const el = document.getElementById(id);
  if (!el) return;
  el.classList.remove("open");
  el.dispatchEvent(new CustomEvent("modal:closed"));
}

document.addEventListener("click", e => {
  if (e.target.classList.contains("modal-overlay")) {
    e.target.classList.remove("open");
    e.target.dispatchEvent(new CustomEvent("modal:closed"));
  }
});

document.addEventListener("keydown", e => {
  if (e.key === "Escape") {
    document.querySelectorAll(".modal-overlay.open").forEach(m => {
      m.classList.remove("open");
      m.dispatchEvent(new CustomEvent("modal:closed"));
    });
  }
});

/* ── Toast notifications ────────────────────────────────────── */

function toast(message, type = "info") {
  const container = document.getElementById("toast-container");
  const el = document.createElement("div");
  el.className = `toast ${type}`;
  const iconMap = { success: icons.checkCircle, error: icons.xCircle, info: icons.info };
  el.innerHTML = `<span>${iconMap[type] || icons.info}</span><span>${esc(message)}</span>`;
  container.appendChild(el);
  setTimeout(() => el.remove(), 4000);
}

/* ── Escape helper ──────────────────────────────────────────── */

function esc(str) {
  if (str == null) return "";
  return String(str)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

/* ── Tenant change ──────────────────────────────────────────── */

document.getElementById("tenant-select").addEventListener("change", () => {
  const active = document.querySelector(".nav-item.active");
  if (active) showPage(active.dataset.page);
});

/* ── Auth UI init ───────────────────────────────────────────── */

function initAuthUI() {
  const user = getUser();
  if (!user) { logout(); return; }

  // Populate tenant selector from user's tenants
  const sel = document.getElementById("tenant-select");
  if (sel && user.tenants && user.tenants.length > 0) {
    sel.innerHTML = user.tenants
      .map(t => `<option value="${esc(t.slug)}">${esc(t.name || t.slug)}</option>`)
      .join("");
  }

  // Show user name in sidebar footer
  const nameEl = document.getElementById("sidebar-user-name");
  if (nameEl) nameEl.textContent = user.name || user.email;

  // Show/hide admin-only items based on role
  const isAdmin = user.role === "admin";
  document.querySelectorAll(".admin-only").forEach(el => {
    el.style.display = isAdmin ? "" : "none";
  });
}

